public class PythagorasModel {

    private double seiteA;
    private double seiteB;
    private double seiteC;
    private double beta;
    private double faktor;
    private double minSize;

    private int farbariante;
    private int verzweigungsvariante;

    public double getSeiteA() {
        return seiteA;
    }

    public void setSeiteA(double seiteA) {
        this.seiteA = seiteA;
    }

    public double getSeiteB() {
        return seiteB;
    }

    public void setSeiteB(double seiteB) {
        this.seiteB = seiteB;
    }

    public double getSeiteC() {
        return seiteC;
    }

    public void setSeiteC(double seiteC) {
        this.seiteC = seiteC;
    }

    public double getBeta() {
        return beta;
    }

    public void setBeta(double beta) {
        this.beta = beta;
    }

    public double getFaktor() {
        return faktor;
    }

    public void setFaktor(double faktor) {
        this.faktor = faktor;
    }

    public double getMinSize() {
        return minSize;
    }

    public void setMinSize(double minSize) {
        this.minSize = minSize;
    }

    public int getFarbariante() {
        return farbariante;
    }

    public void setFarbariante(int farbariante) {
        this.farbariante = farbariante;
    }

    public int getVerzweigungsvariante() {
        return verzweigungsvariante;
    }

    public void setVerzweigungsvariante(int verzweigungsvariante) {
        this.verzweigungsvariante = verzweigungsvariante;
    }



}
